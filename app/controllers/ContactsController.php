	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
            $contact = new Contact();
            $contact->fName = Input::get('fName');
            $contact->lName = Input::get('lName');
            $contact->email = Input::get('email');
            $contact->save();
            return $contact;
	}

